#include <unistd.h>

void	ft_print_bits(unsigned char octet)
{
	unsigned char i;

	i = 128;
	while (i > 0)
	{
		if (i & octet)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		i >>= 1;
	}
}
